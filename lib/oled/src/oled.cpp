static const uint16_t ADDRESS = 0x78 >> 1;
static const uint8_t CMD = 0x00;
static const uint8_t DATA = 0x40;

inline uint8_t send(uint8_t type, uint8_t data) {
  Wire.beginTransmission(ADDRESS);
  Wire.write(type);
  Wire.write(data);
  return Wire.endTransmission();
}

inline uint8_t send(uint8_t type, const uint8_t* data, uint16_t size) {
  Wire.beginTransmission(ADDRESS);
  Wire.write(type);

  for (auto i = 0; i < size; i++) {
    Wire.write(data[i]);
  }
  return Wire.endTransmission();
}

inline uint8_t OLEDClass::setCursor(uint8_t x, uint8_t y) {
  // set Y position
  return send(CMD, 0xB0 | y) ||
         send(CMD, 0x10 | ((x & 0xF0) >> 4)) ||  // set X position high 4 bits
         send(CMD, 0x00 | (x & 0x0F));           // set X position low 4 bits
}

uint8_t OLEDClass::clear() {
  for (uint8_t i = 0; i < 8; i++) {
    if (auto ret = setCursor(0, i)) {
      return ret;
    }

    for (uint8_t j = 0; j < 128; j++) {
      if (auto ret = send(DATA, 0x00)) {
        return ret;
      }
    }
  }
  return 0;
}

uint8_t OLEDClass::drawChar(uint8_t row, uint8_t col, char c) {
  // 8 * 16
  for (uint8_t pos = 0; pos < 2; pos++) {
    c = toupper(c);
    if (c < ' ' || c > ' ' + 63) {
      return 1;
    }

    if (auto ret = setCursor(col * 8, row * 2 + pos)) {
      return ret;
    }

    const uint8_t* data = fonts[c - ' '] + pos * 8;

    if (auto ret = send(DATA, data, 8)) {
      return ret;
    }
  }

  return 0;
}

uint8_t OLEDClass::drawString(uint8_t row, uint8_t col, const char* str) {
  if (col >= 16 || row > 3) {
    return 1;
  }

  for (uint8_t i = 0; (i < 16 - col) && (str[i] != '\0'); i++) {
    if (drawChar(row, col + i, str[i])) {
      return 1;
    }
  }
  return 0;
}

uint8_t OLEDClass::drawBin(uint8_t row, uint8_t col, uint8_t* data,
                           uint8_t size) {
  char buf[18];
  if (size > 16 - col) {
    size = 16;
  }

  for (uint8_t i = 0; i < size; i++) {
    snprintf(buf + i * 2, 4, "%02X", data[i]);
  }

  return drawString(row, col, buf);
}

uint8_t OLEDClass::begin() {
  const uint8_t cmds[] = {
      0xAE,  // 关闭显示
      0xD5,  // 设置显示时钟分频比/振荡器频率
      0x80,
      0xA8,  // 设置多路复用率
      0x3F,
      0xD3,  // 设置显示偏移
      0x00,
      0x40,  // 设置显示开始行
      0xA1,  // 设置左右方向，0xA1正常 0xA0左右反置
      0xC8,  // 设置上下方向，0xC8正常 0xC0上下反置
      0xDA,  // 设置COM引脚硬件配置
      0x12,
      0x81,  // 设置对比度控制
      0xCF,
      0xD9,  // 设置预充电周期
      0xF1,
      0xDB,  // 设置VCOMH取消选择级别
      0x30,
      0xA4,  // 设置整个显示打开/关闭
      0xA6,  // 设置正常/倒转显示
      0x8D,  // 设置充电泵
      0x14,
      0xAF,  // 开启显示
  };

  for (size_t i = 0; i < sizeof(cmds); i++) {
    if (auto ret = send(CMD, cmds[i])) {
      return ret;
    }
  }

  return clear();
}

uint8_t OLEDClass::drawScreen(uint8_t data[128 * 8]) {
  for (uint8_t y = 0; y < 8; y++) {
    if (setCursor(0, y)) {
      return 1;
    }

    for (uint8_t x = 0; x < 128; x++) {
      if (send(DATA, *data++)) {
        return 1;
      }
    }
  }
  return 0;
}

